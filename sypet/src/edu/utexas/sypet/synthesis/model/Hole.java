package edu.utexas.sypet.synthesis.model;

public class Hole {

	public Hole(int i, String t) {
		type = t;
		id = i;
	}

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getType() {
		return type;
	}

	public void setType(String type) {
		this.type = type;
	}

	private int id;

	private String type;

	public DefVar getVar() {
		return var;
	}

	public void setVar(DefVar var) {
		this.var = var;
	}

	public String toString() {
		return "hole" + id;
	}

	private DefVar var;
}
